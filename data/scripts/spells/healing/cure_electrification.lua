local combat = Combat()
combat:setParameter(COMBAT_PARAM_EFFECT, CONST_ME_MAGIC_BLUE)
combat:setParameter(COMBAT_PARAM_DISPEL, CONDITION_ENERGY)
combat:setParameter(COMBAT_PARAM_AGGRESSIVE, false)

local spell = Spell("instant")

function spell.onCastSpell(creature, variant)
	return combat:execute(creature, variant)
end

spell:name("Cure Electrification")
spell:words("exana vis")
spell:group("healing")
spell:vocation("druid;true", "elder druid;true", "celtic druid;true", "spirit healer;true", "sorcerer;true", "master sorcerer;true", "archmage;true", "arcane wizard;true")
spell:castSound(SOUND_EFFECT_TYPE_SPELL_CURE_ELECTRIFICATION)
spell:id(146)
spell:cooldown(6000)
spell:groupCooldown(1000)
spell:level(30)
spell:mana(30)
spell:isSelfTarget(true)
spell:isAggressive(false)
spell:isPremium(false)
spell:needLearn(false)
spell:register()
